/*
It's defining a custom error class called ApiError which extends the built-in JavaScript Error class.
This helps us create detailed and consistent error messages in our APIs instead of using plain errors like new Error("Something broke").
*/

class ApiError extends Error {
  constructor(
    statusCode,
    message = "Something went wrong",
    errors = [],
    stack = ""
  ) {
    super(message);
    this.statusCode = statusCode;
    this.data = null;
    this.message = message;
    this.success = false;
    this.errors = errors;

    if (stack) {
      this.stack = stack;
    } else {
      Error.captureStackTrace(this, this.constructor);
    }
  }
}

export { ApiError };
